package com.addictox.fndx.model;

public class News {
    public String title;
    public String desc;
    public String pub;
    public String img;
    public String link;

    public News(String title, String desc, String pub, String img, String link){
        this.title = title;
        this.desc = desc;
        this.pub = pub;
        this.img = img;
        this.link = link;
    }
}
